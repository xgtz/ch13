package JavaBean;

public class MyDayBean {
	private String day;
	private String thing;
	
	public MyDayBean(){
		
	}



	public String getDay() {
		return day;
	}



	public void setDay(String day) {
		this.day = day;
	}



	public String getThing() {
		return thing;
	}

	public void setThing(String thing) {
		this.thing = thing;
	}
	
	
	
	
}
